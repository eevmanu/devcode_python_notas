# Uso del casting

# int()

x = int(1)
print(x)
print(type(x))

x = int(1.2)
print(x)
print(type(x))

x = int('4')
print(x)
print(type(x))

x = int('4.3')
print(x)
print(type(x))

y = int('hola')
print(y)
print(type(y))

